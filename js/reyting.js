let filteredData = [];

const studentsGrid = document.getElementById("studentsGrid");
const searchInput = document.getElementById("searchInput");
const groupFilter = document.getElementById("groupFilter");
const sortFilter = document.getElementById("sortFilter");
const statsBar = document.getElementById("statsBar");

function init() {
  populateGroupFilter();
  updateStats();
  filterAndRender();

  searchInput.addEventListener("input", filterAndRender);
  groupFilter.addEventListener("change", filterAndRender);
  sortFilter.addEventListener("change", filterAndRender);
}

function populateGroupFilter() {
  const groups = [...new Set(STUDENTS_DATA.map((s) => s.group))].filter(
    (g) => g > 0,
  );
  groups.sort((a, b) => a - b);

  groups.forEach((group) => {
    const option = document.createElement("option");
    option.value = group;
    option.textContent = `Guruh ${group}`;
    groupFilter.appendChild(option);
  });
}

function updateStats() {
  if (STUDENTS_DATA.length === 0) return;

  const totalStudents = STUDENTS_DATA.length;
  const avgScore = (
    STUDENTS_DATA.reduce((sum, s) => sum + s.total_score, 0) / totalStudents
  ).toFixed(1);
  const maxScore = Math.max(...STUDENTS_DATA.map((s) => s.total_score));
  const groups = new Set(STUDENTS_DATA.map((s) => s.group)).size;

  statsBar.innerHTML = `
                <div class="stat-card">
                    <div class="stat-number">${totalStudents}</div>
                    <div class="stat-label"><i style="color: #ffffff;" class="fa-solid fa-check-double"></i> Jami O'quvchilar</div>
                </div>
                <div class="stat-card">
                    <div class="stat-number">${avgScore}</div>
                    <div class="stat-label"><i style="color: #14b8ff;" class="fa-solid fa-gauge"></i> O'rtacha Ball</div>
                </div>
                <div class="stat-card">
                    <div class="stat-number">${maxScore}</div>
                    <div class="stat-label"><i style="color: #f97316;" class="fa-solid fa-fire"></i> Eng Yuqori Ball</div>
                </div>
                <div class="stat-card">
                    <div class="stat-number">${groups}</div>
                    <div class="stat-label"><i style="color: #8b5cf6;" class="fa-solid fa-users"></i>  Guruhlar</div>
                </div>
            `;
}

function filterAndRender() {
  let data = [...STUDENTS_DATA];

  const searchTerm = searchInput.value.toLowerCase().trim();
  if (searchTerm) {
    data = data.filter(
      (s) =>
        s.name.toLowerCase().includes(searchTerm) ||
        s.group.toString().includes(searchTerm),
    );
  }

  const selectedGroup = groupFilter.value;
  if (selectedGroup) {
    data = data.filter((s) => s.group === parseInt(selectedGroup));
  }

  const sortBy = sortFilter.value;
  switch (sortBy) {
    case "score-desc":
      data.sort((a, b) => b.total_score - a.total_score);
      break;
    case "score-asc":
      data.sort((a, b) => a.total_score - b.total_score);
      break;
    case "name-asc":
      data.sort((a, b) => a.name.localeCompare(b.name));
      break;
    case "name-desc":
      data.sort((a, b) => b.name.localeCompare(a.name));
      break;
  }

  filteredData = data;
  renderStudents();
}

function renderStudents() {
  if (filteredData.length === 0) {
    studentsGrid.innerHTML = `
                    <div class="empty-state">
                        <div class="icon"><i class="fa-solid fa-magnifying-glass"></i></div>
                        <h3>Natija topilmadi</h3>
                        <p>Qidiruv parametrlarini o'zgartiring</p>
                    </div>
                `;
    return;
  }

  studentsGrid.innerHTML = filteredData
    .map((student, index) => {
      const rank = index + 1;
      let rankClass = "regular";

      if (rank === 1) rankClass = "gold";
      else if (rank === 2) rankClass = "silver";
      else if (rank === 3) rankClass = "bronze";

      return `
                    <div class="student-card">
                        <div class="rank-badge ${rankClass}">
                            ${rank}
                        </div>
                        <div class="student-info">
                            <div class="student-name">${student.name}</div>
                            <div class="student-meta">
                                <span><i class="fa-solid fa-users"></i> Guruh: ${student.group}</span>
                            </div>
                        </div>
                        <div class="score-display">
                            <div class="score">${student.total_score}</div>
                            <div class="score-label">BALL</div>
                        </div>
                    </div>
                `;
    })
    .join("");
}

